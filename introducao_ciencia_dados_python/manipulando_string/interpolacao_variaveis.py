# Interpolação de variáveis 

nome = "Maria Laura"
idade = 18
profissao = "Programador"
linguagem = "Python"

# % 

print("Olá, me chamo %s, tenho %d anos, e sou %s da linguagem %s" % (nome, idade, profissao, linguagem))

# Método format

print("Olá, me chamo {}, tenho {} anos, sou {} da linguagem {}".format(nome, idade, profissao, linguagem))

# f-string

print(f"Olá, me chamo {nome}. Eu tenho {idade} anos, sou {profissao} de {linguagem}")

PI = 3.14159
print(f"O valor de PI: {PI:.2f} ")

